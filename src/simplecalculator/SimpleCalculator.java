    private void EquActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EquActionPerformed
        // TODO add your handling code here:
        switch(operator){
            case "Sum":
                result = Double.parseDouble(x1) + Double.parseDouble(x2);
                break;
            case "Diff":
                result = Double.parseDouble(x1) - Double.parseDouble(x2);
                break;
            case "Mul":
                result = Double.parseDouble(x1) * Double.parseDouble(x2);
                break;
            case "Div":
                result = Double.parseDouble(x1) / Double.parseDouble(x2);
                break;
        }
        text = Double.toString(result);
        resultText.setText(text);
        x1 = Double.toString(result);
        x2 = "";
        operatorPressed = false;
    }//GEN-LAST:event_EquActionPerformed

